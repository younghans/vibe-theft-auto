function choose(list, rng) {
  if (!Array.isArray(list) || list.length <= 0) {
    return null;
  }
  return list[Math.floor(rng() * list.length) % list.length] ?? list[0];
}

export function normalizeSchoolGeographyCountryName(value = '') {
  return String(value ?? '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/gu, '')
    .toLowerCase()
    .replace(/&/gu, ' and ')
    .replace(/\bu\s*s\s*a\b/gu, 'usa')
    .replace(/\bu\s*k\b/gu, 'uk')
    .replace(/\bst\b/gu, 'saint')
    .replace(/[^a-z0-9]+/gu, ' ')
    .replace(/\bthe\b/gu, ' ')
    .replace(/\s+/gu, ' ')
    .trim();
}

export function getSchoolGeographyCountryAliases(country = null) {
  if (!country) {
    return [];
  }
  return [country.name, ...(Array.isArray(country.aliases) ? country.aliases : [])]
    .map((alias) => String(alias ?? '').trim())
    .filter(Boolean);
}

export function isSchoolGeographyCountryAnswer(country = null, guess = '') {
  const normalizedGuess = normalizeSchoolGeographyCountryName(guess);
  if (!normalizedGuess) {
    return false;
  }
  return getSchoolGeographyCountryAliases(country)
    .map((alias) => normalizeSchoolGeographyCountryName(alias))
    .some((alias) => alias === normalizedGuess);
}

export function createSchoolGeographyCountry({ rng = Math.random } = {}) {
  const country = choose(SCHOOL_GEOGRAPHY_TARGET_COUNTRIES, rng) ?? SCHOOL_GEOGRAPHY_TARGET_COUNTRIES[0];
  return {
    id: country.id,
    name: country.name,
    lat: country.lat,
    lon: country.lon,
    aliases: [...country.aliases]
  };
}
